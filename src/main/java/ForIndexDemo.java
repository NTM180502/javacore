public class ForIndexDemo {
    public static void main(String[] args) {
//        in ra 1--> 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
//        in ra số chẳn
        for (int i = 2; i <= 10; i += 2) {
            System.out.println(i);
        }
//        từ 10-->1
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
